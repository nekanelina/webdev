const mongoose = require("mongoose");

const serviceSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  role: {
    type: String,
    required: true,
  },
});

const services = mongoose.model("services", serviceSchema);

module.exports = services;
